<b>Sejarah Lembaga Pendidikan Tinggi Y.A.I</b>

<p>Tumbuh dan berkembang menjadi lebih besar dan terbaik merupakan tujuan Keluarga Besar Yayasan Administrasi Indonesia (Y.A.I). Sejarah pengabdian Y.A.I yang dimulai tahun 1972 dengan menyelenggarakan Kursus Akuntansi dengan murid puluhan jumlahnya, kini telah berkembang menjadi tiga Lembaga Pendidikan Tinggi Y.A.I (LPT Y.A.I) dengan multi program studi dan multi strata.</p>

 
<ul style="padding:20px">
<b>Ketiga LPT Y.A.I tersebut adalah:</b>

<li>Akademi Akuntansi Y.A.I (AA Y.A.I)</li>

<li>Sekolah Tinggi Ilmu Ekonomi Y.A.I (STIE Y.A.I)</li>

<li>Universitas Persada Indonesia Y.A.I (UPI Y.A.I)</li>

 </ul>

<p>Jumlah mahasiswa untuk ketiga LPT Y.A.I sampai saat ini telah mencapai kurang lebih 20.000 orang. Dimulai dengan menyewa fasilitas pendidikan, namun pada saat ini telah mempunyai 4 buah kampus yang dilengkapi dengan peralatan pendidikan modern. Sungguhpun demikian, Y.A.I tidak melupakan cikal bakalnya, bahkan tetap mengembangkan kursus-kursus Y.A.I, yang setiap tahunnya mempunyai murid lebih kurang 1.000 siswa, yang tersebar di cabang-cabang kursus di kelima wilayah DKI Jakarta.</p>

<p>Program-program studi di lingkungan LPT Y.A.I ternyata mendapat sambutan yang sangat positif dari dunia bisnis, industri, pemerintah dan masyarakat. Hal ini terbukti dengan tersebarnya alumni LPT Y.A.I di berbagai sektor kegiatan bisnis dan makin meningkatnya jumlah mahasiswa baru yang mempercayakan masa depannya melalui pendidikan formal di lingkungan LPT Y.A.I.</p>

<p>Demi kesinambungan jenjang pendidikan, Y.A.I telah membuka program pasca sarjana strata 2 dan strata 3 dengan program-program studi Magister Manajemen, Akuntansi dan Psikologi dan program Doktor Ekonomi dan Psikologi. Program pasca sarjana tersebut untuk mewadahi keinginan masyarakat dan alumni demi mencapai jenjang pendidikan yang lebih tinggi.</p>


<p>Kepercayaan pemerintah terhadap LPT Y.A.I diwujudkan dengan memberikan status Terakreditasi A dan B kepada seluruh program studi di LPT Y.A.I. Di dalam melaksanakan pendidikannya, LPT Y.A.I memiliki pandangan jauh ke depan untuk ikut mencerdaskan kehidupan bangsa.</p>

 

<b>Visi LPT Y.A.I</b>

<p>Menjadi Lembaga Pendidikan Tinggi unggulan yang melaksanakan Tri Dharma Perguruan Tinggi dan mampu menghasilkan alumni-alumni yang bermutu tinggi, berbudi luhur serta bersikap mental yang profesional, berwawasan global, menumbuh kembangkan ilmu pengetahuan dan teknologi serta memberikan sumbangan ilmu pengetahuan tersebut bagi kepentingan kemajuan bangsa dan negara.</p>


